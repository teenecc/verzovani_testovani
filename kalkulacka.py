class Kalkulacka:

    def scitani(self, a, b):
        """
        Add two numbers
        :param a: First number
        :param b: Second number
        :return: Sum of number a and number b
        """
        return a + b

    def odecitani(self, a, b):
        """
        Odecita dve cisla
        :param a: První číslo
        :param b: Druhé číslo
        :return: Rozdíl
        """
        return a - b

    def nasobeni(self, a, b):
        """
        Násobí čísla
        :param a: první číslo
        :param b: druhé číslo
        :return: Součin
        """
        return a * b

    def deleni(self, a, b):
        """
        Dělí první číslo druhým číslem
        :param a: první číslo
        :param b: druhé číslo
        :return: podíl
        """
        return a / b
