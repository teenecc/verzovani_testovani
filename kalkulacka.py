class Kalkulacka:

    def scitani(self, a, c):
        """
        Add two numbers
        :param a: First number
        :param c: Second number
        :return: Sum of number a and number b
        """
        return a + c

    def odecitani(self, a, c):
        """
        Odecita dve cisla
        :param a: První číslo
        :param c: Druhé číslo
        :return: Rozdíl
        """
        return a - c

    def nasobeni(self, a, c):
        """
        Násobí čísla
        :param a: první číslo
        :param c: druhé číslo
        :return: Součin
        """
        return a * c

    def deleni(self, a, c):
        """
        Dělí první číslo druhým číslem
        :param a: první číslo
        :param c: druhé číslo
        :return: podíl
        """
        return a / c

